package chapter6.lights;

/**
 * The type Light.
 */
public class Light {

    /**
     * Instantiates a new Light.
     */
    public Light() {
        // empty
    }

    /**
     * On.
     */
    public void on() {
        System.out.println("Lights ON");
    }

    /**
     * Off.
     */
    public void off() {
        System.out.println("Lights OFF");
    }
}
